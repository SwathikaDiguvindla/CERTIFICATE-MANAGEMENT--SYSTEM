class Student {
    constructor(id, name, email, phone, domain, start_date, end_date) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.domain = domain;
        this.start_date = start_date;
        this.end_date = end_date;
    }
}

module.exports = Student;